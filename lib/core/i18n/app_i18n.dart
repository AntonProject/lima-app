  int _pluralIndex(String localeKey, int n) {
    switch (localeKey) {
      case 'ru':
        final mod10 = n % 10;
        final mod100 = n % 100;
        if (mod10 == 1 && mod100 != 11) return 0;
        if (mod10 >= 2 && mod10 <= 4 && !(mod100 >= 12 && mod100 <= 14)) {
          return 1;
        }
        return 2;
      case 'en':
        return n == 1 ? 0 : 1;
      default: // uz and any single-form language
        return 0;
    }
  }

  /// Returns just the correct plural noun form for [n] in the current locale,
  /// without the number (e.g. `документов`). [noun] is a key in [_plurals].
  String pluralWord(int n, String noun) {
    final localeKey = _localeKey();
    final forms = _plurals[localeKey]?[noun] ?? _plurals['ru']![noun] ?? [noun];
    final idx = _pluralIndex(localeKey, n).clamp(0, forms.length - 1);
    return forms[idx];
  }

  /// Returns the count followed by the correct plural form for the current
  /// locale, e.g. `5 документов`. [noun] is a key in [_plurals]
  /// (e.g. 'documents', 'visits').
  String plural(int n, String noun) => '$n ${pluralWord(n, noun)}';
