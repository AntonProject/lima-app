  String _localeKey() =>
      locale.languageCode == 'uz' && locale.scriptCode == 'Cyrl'
          ? 'uz_cyrl'
          : locale.languageCode;

  String t(String key, {Map<String, String>? args}) {
    final localeKey = _localeKey();
    final lang = _v[localeKey] ?? _v['ru']!;
    var value = lang[key] ?? _v['ru']![key] ?? key;
    if (args != null) {
      args.forEach((k, v) => value = value.replaceAll('{$k}', v));
    }
    return value;
  }

